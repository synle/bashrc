import { describe, it, expect } from "vitest";
import { getIndexFunction, fileSystem, mockFsExistence } from "./setup.js";

const touchFile = getIndexFunction("touchFile");
const backupText = getIndexFunction("backupText");
const writeTextIfSignificantChange = getIndexFunction("writeTextIfSignificantChange");
const writeJsonWithMerge = getIndexFunction("writeJsonWithMerge");
const replaceTextLineByLine = getIndexFunction("replaceTextLineByLine");
const safeWriteText = getIndexFunction("safeWriteText");
const getAutoGeneratedText = getIndexFunction("getAutoGeneratedText");

describe("touchFile", () => {
  it("should create file when it does not exist", () => {
    const filePath = "/mock/home/newfile.txt";
    touchFile(filePath, "default content");
    // touchFile uses fs.writeFileSync directly (via mockFs), check fileSystem
    expect(fileSystem[filePath] || "").toBeDefined();
  });

  it("should not overwrite file when it already exists", () => {
    const filePath = "/mock/home/existing.txt";
    mockFsExistence[filePath] = true;
    fileSystem[filePath] = "old content";
    touchFile(filePath, "new content");
    expect(fileSystem[filePath]).toBe("old content");
  });
});

describe("backupText", () => {
  it("should create backup and update file when content differs", () => {
    const filePath = "/mock/home/hosts";
    fileSystem[filePath] = "old hosts content";

    backupText(filePath, "new hosts content");

    expect(fileSystem[filePath]).toBe("new hosts content");
    // a backup file should also have been created with a timestamp key
    const backupKeys = Object.keys(fileSystem).filter((k) => k.startsWith(filePath + ".") && k !== filePath);
    expect(backupKeys.length).toBeGreaterThanOrEqual(1);
    expect(fileSystem[backupKeys[0]]).toBe("old hosts content");
  });

  it("should skip backup when content is the same", () => {
    const filePath = "/mock/home/hosts";
    fileSystem[filePath] = "same content";

    const keysBefore = Object.keys(fileSystem).length;
    backupText(filePath, "same content");
    const keysAfter = Object.keys(fileSystem).length;

    expect(keysAfter).toBe(keysBefore);
  });
});

describe("writeTextIfSignificantChange", () => {
  it("should write when file is new (empty)", () => {
    const filePath = "/mock/home/newfile.txt";
    writeTextIfSignificantChange(filePath, "brand new content");
    expect(fileSystem[filePath]).toBe("brand new content");
  });

  it("should skip when content is identical", () => {
    const filePath = "/mock/home/samefile.txt";
    fileSystem[filePath] = "identical content";
    writeTextIfSignificantChange(filePath, "identical content");
    expect(fileSystem[filePath]).toBe("identical content");
  });

  it("should write when change exceeds threshold", () => {
    const filePath = "/mock/home/bigchange.txt";
    fileSystem[filePath] = "short";
    writeTextIfSignificantChange(filePath, "this is a much longer string with significant changes", 0.1);
    expect(fileSystem[filePath]).toBe("this is a much longer string with significant changes");
  });

  it("should skip when change is below threshold", () => {
    const filePath = "/mock/home/smallchange.txt";
    // 100 chars original, change by 1 char = 1% < 10% threshold
    const original = "a".repeat(100);
    fileSystem[filePath] = original;
    writeTextIfSignificantChange(filePath, original + "b", 0.1);
    expect(fileSystem[filePath]).toBe(original);
  });
});

describe("writeJsonWithMerge", () => {
  it("should merge new properties into existing JSON", () => {
    const filePath = "/mock/home/config.json";
    fileSystem[filePath] = '{"name": "test", "version": 1}';
    writeJsonWithMerge(filePath, { version: 2, author: "someone" });
    const result = JSON.parse(fileSystem[filePath]);
    expect(result.name).toBe("test");
    expect(result.version).toBe(2);
    expect(result.author).toBe("someone");
  });

  it("should create new JSON when file does not exist", () => {
    const filePath = "/mock/home/new.json";
    writeJsonWithMerge(filePath, { key: "value" });
    const result = JSON.parse(fileSystem[filePath]);
    expect(result.key).toBe("value");
  });
});

describe("replaceTextLineByLine", () => {
  it("should replace matching patterns line by line", () => {
    const filePath = "/mock/home/script.sh";
    fileSystem[filePath] = "FOO=old\nBAR=keep\nFOO=also_old";

    replaceTextLineByLine(filePath, [[/FOO=\w+/, "FOO=new"]]);

    expect(fileSystem[filePath]).toBe("FOO=new\nBAR=keep\nFOO=new");
  });

  it("should create .bak backup", () => {
    const filePath = "/mock/home/file.conf";
    fileSystem[filePath] = "original";
    replaceTextLineByLine(filePath, [[/original/, "replaced"]]);
    expect(fileSystem[`${filePath}.bak`]).toBe("original");
    expect(fileSystem[filePath]).toBe("replaced");
  });

  it("should handle multiple replacement patterns", () => {
    const filePath = "/mock/home/multi.txt";
    fileSystem[filePath] = "hello world foo";

    replaceTextLineByLine(filePath, [
      [/hello/, "hi"],
      [/foo/, "bar"],
    ]);

    expect(fileSystem[filePath]).toBe("hi world bar");
  });
});

describe("safeWriteText", () => {
  it("should write content when it is valid", () => {
    const filePath = "/mock/home/safe.txt";
    safeWriteText(filePath, "valid content", "backup content");
    expect(fileSystem[filePath]).toBe("valid content");
  });

  it("should throw when content is empty", () => {
    expect(() => safeWriteText("/mock/home/safe.txt", "", "backup")).toThrow("generated content is empty");
  });

  it("should throw when content is whitespace only", () => {
    expect(() => safeWriteText("/mock/home/safe.txt", "   ", "backup")).toThrow("generated content is empty");
  });

  it("should throw when content is too small compared to backup", () => {
    const backup = "a".repeat(1000);
    expect(() => safeWriteText("/mock/home/safe.txt", "tiny", backup, 0.1)).toThrow("backup size");
  });

  it("should write when content passes minimum ratio", () => {
    const backup = "a".repeat(100);
    const newContent = "b".repeat(50);
    safeWriteText("/mock/home/safe.txt", newContent, backup, 0.1);
    expect(fileSystem["/mock/home/safe.txt"]).toBe(newContent);
  });
});

describe("getAutoGeneratedText", () => {
  it("should return a string with auto-generated note", () => {
    const result = getAutoGeneratedText();
    expect(result).toContain("NOTE: STOP - do not edit by hand");
    expect(result).toContain("auto-generated");
  });

  it("should contain a date-like pattern", () => {
    const result = getAutoGeneratedText();
    // matches YYYY-MM-DD pattern
    expect(result).toMatch(/\d{4}-\d{2}-\d{2}/);
  });

  it("should contain AM or PM", () => {
    const result = getAutoGeneratedText();
    expect(result).toMatch(/(AM|PM)/);
  });
});
